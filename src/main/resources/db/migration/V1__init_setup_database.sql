CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TYPE auth_provider AS ENUM ('LOCAL', 'GOOGLE');
CREATE TYPE user_role AS ENUM ('USER', 'CREATOR', 'ADMIN');
CREATE TYPE user_status AS ENUM ('ACTIVE', 'BANNED', 'DELETED');
CREATE TYPE rank_level AS ENUM ('BRONZE', 'SILVER', 'GOLD', 'DIAMOND');

CREATE TYPE wallet_tx_type AS ENUM (
    'DEPOSIT',
    'WITHDRAW',
    'BUY_DOCUMENT',
    'SELL_DOCUMENT',
    'DOWNLOAD_REWARD',
    'REFUND'
);

CREATE TYPE tx_direction AS ENUM ('IN', 'OUT');
CREATE TYPE tx_status AS ENUM ('PENDING', 'SUCCESS', 'FAILED', 'CANCELLED');

CREATE TYPE document_status AS ENUM ('PENDING', 'APPROVED', 'REJECTED', 'HIDDEN');
CREATE TYPE document_price_type AS ENUM ('FREE', 'COIN', 'VND');

CREATE TYPE payment_provider AS ENUM ('PAYOS', 'VNPAY', 'MOMO');
CREATE TYPE payment_status AS ENUM ('PENDING', 'SUCCESS', 'FAILED', 'CANCELLED');

CREATE TYPE friend_status AS ENUM ('PENDING', 'ACCEPTED', 'BLOCKED');
CREATE TYPE conv_type AS ENUM ('PRIVATE', 'GROUP');
CREATE TYPE msg_type AS ENUM ('TEXT', 'IMAGE', 'FILE', 'SYSTEM');
CREATE TYPE member_role AS ENUM ('OWNER', 'ADMIN', 'MEMBER');

CREATE TYPE report_status AS ENUM ('PENDING', 'RESOLVED', 'REJECTED');
CREATE TYPE withdrawal_status AS ENUM ('PENDING', 'APPROVED', 'REJECTED', 'PAID');

