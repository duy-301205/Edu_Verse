CREATE TABLE users (
                       id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                       username VARCHAR(50) NOT NULL UNIQUE,
                       email VARCHAR(100) NOT NULL UNIQUE,
                       password_hash VARCHAR(255),
                       full_name VARCHAR(100),
                       avatar_url TEXT,
                       provider auth_provider DEFAULT 'LOCAL' NOT NULL,
                       role user_role DEFAULT 'USER' NOT NULL,
                       rank_level rank_level DEFAULT 'BRONZE' NOT NULL,
                       contribution_point INT DEFAULT 0 NOT NULL,
                       status user_status DEFAULT 'ACTIVE' NOT NULL,
                       created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                       updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

                       CONSTRAINT check_contribution_point CHECK (contribution_point >= 0)
);

CREATE TABLE wallets (
                         id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                         user_id BIGINT NOT NULL UNIQUE REFERENCES users(id) ON DELETE CASCADE,
                         vnd_balance DECIMAL(15,2) DEFAULT 0 NOT NULL,
                         coin_balance INT DEFAULT 0 NOT NULL,
                         version INT DEFAULT 0 NOT NULL, -- Định danh phiên phục vụ Optimistic Locking trong Spring Boot
                         created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

                         CONSTRAINT check_wallet_balance CHECK (
                             vnd_balance >= 0 AND coin_balance >= 0
                             )
);

CREATE TABLE wallet_transactions (
                                     id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
                                     wallet_id BIGINT NOT NULL REFERENCES wallets(id) ON DELETE CASCADE,
                                     type wallet_tx_type NOT NULL,
                                     direction tx_direction NOT NULL,
                                     amount_vnd DECIMAL(15,2) DEFAULT 0 NOT NULL,
                                     amount_coin INT DEFAULT 0 NOT NULL,
                                     balance_vnd_after DECIMAL(15,2), -- Sao lưu số dư để đối soát (Audit Log)
                                     balance_coin_after INT,
                                     reference_type VARCHAR(50),      -- Ví dụ: 'DOCUMENT_ORDERS', 'PAYMENTS'
                                     reference_id VARCHAR(100),       -- ID của bản ghi liên quan
                                     idempotency_key VARCHAR(100) UNIQUE, -- Chống xử lý trùng lặp giao dịch
                                     status tx_status DEFAULT 'SUCCESS' NOT NULL,
                                     description TEXT,
                                     created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

                                     CONSTRAINT check_tx_amount CHECK (
                                         amount_vnd >= 0 AND amount_coin >= 0
                                         )
);

CREATE TABLE categories (
                            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                            name VARCHAR(100) NOT NULL,
                            type VARCHAR(50), -- Định danh cấp bậc (ví dụ: TRUONG_DAI_HOC, MON_HOC)
                            parent_id BIGINT REFERENCES categories(id) ON DELETE SET NULL,
                            created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE documents (
                           id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                           title VARCHAR(255) NOT NULL,
                           description TEXT,
                           file_url TEXT NOT NULL,       -- Link tải file gốc trên S3/MinIO
                           preview_url TEXT,            -- Link file băm xem trước (3 trang đầu)
                           file_size BIGINT,
                           file_type VARCHAR(50) DEFAULT 'PDF',
                           price_type document_price_type DEFAULT 'FREE' NOT NULL,
                           price_coin INT DEFAULT 0 NOT NULL,
                           price_vnd DECIMAL(15,2) DEFAULT 0 NOT NULL,
                           download_count INT DEFAULT 0 NOT NULL,
                           view_count INT DEFAULT 0 NOT NULL,
                           uploader_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
                           category_id BIGINT NOT NULL REFERENCES categories(id),
                           status document_status DEFAULT 'PENDING' NOT NULL,
                           reject_reason TEXT,
                           created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                           updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

                           CONSTRAINT check_document_price CHECK (
                               (price_type = 'FREE' AND price_coin = 0 AND price_vnd = 0) OR
                               (price_type = 'COIN' AND price_coin > 0 AND price_vnd = 0) OR
                               (price_type = 'VND' AND price_vnd > 0 AND price_coin = 0)
                               ),
                           CONSTRAINT check_document_counter CHECK (
                               download_count >= 0 AND view_count >= 0
                               )
);

CREATE TABLE document_downloads (
                                    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                    user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
                                    document_id BIGINT NOT NULL REFERENCES documents(id) ON DELETE CASCADE,
                                    downloaded_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                                    reward_counted BOOLEAN DEFAULT FALSE NOT NULL -- Đánh dấu đã cộng xu cho tác giả chưa (chống spam)
);

CREATE TABLE document_orders (
                                 id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                 buyer_id BIGINT NOT NULL REFERENCES users(id),
                                 seller_id BIGINT NOT NULL REFERENCES users(id),
                                 document_id BIGINT NOT NULL REFERENCES documents(id),
                                 price_vnd DECIMAL(15,2) DEFAULT 0 NOT NULL,
                                 price_coin INT DEFAULT 0 NOT NULL,
                                 platform_fee_vnd DECIMAL(15,2) DEFAULT 0 NOT NULL,
                                 platform_fee_coin INT DEFAULT 0 NOT NULL,
                                 status tx_status DEFAULT 'SUCCESS' NOT NULL,
                                 created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

                                 CONSTRAINT check_not_buy_own_document CHECK (buyer_id <> seller_id)
);

CREATE TABLE document_reports (
                                  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                  document_id BIGINT NOT NULL REFERENCES documents(id) ON DELETE CASCADE,
                                  reporter_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
                                  reason TEXT NOT NULL,
                                  status report_status DEFAULT 'PENDING' NOT NULL,
                                  admin_note TEXT,
                                  created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                                  resolved_at TIMESTAMP WITH TIME ZONE
);

CREATE TABLE payments (
                          id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
                          order_code VARCHAR(100) NOT NULL UNIQUE, -- Mã đơn chuyển sang PayOS/VNPAY
                          amount_vnd DECIMAL(15,2) NOT NULL,
                          coin_amount INT NOT NULL,
                          provider payment_provider NOT NULL,
                          checkout_url TEXT,
                          qr_code_url TEXT,
                          status payment_status DEFAULT 'PENDING' NOT NULL,
                          raw_webhook JSONB, -- Lưu log thô từ ngân hàng bắn về để đối soát khi lỗi
                          created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                          paid_at TIMESTAMP WITH TIME ZONE,

                          CONSTRAINT check_payment_amount CHECK (
                              amount_vnd > 0 AND coin_amount > 0
                              )
);

CREATE TABLE withdrawal_requests (
                                     id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                     user_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
                                     amount_vnd DECIMAL(15,2) NOT NULL,
                                     bank_name VARCHAR(100) NOT NULL,
                                     bank_account_no VARCHAR(50) NOT NULL,
                                     bank_account_name VARCHAR(100) NOT NULL,
                                     status withdrawal_status DEFAULT 'PENDING' NOT NULL,
                                     admin_note TEXT,
                                     created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                                     processed_at TIMESTAMP WITH TIME ZONE,

                                     CONSTRAINT check_withdraw_amount CHECK (amount_vnd > 0)
);

CREATE TABLE friendships (
                             id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                             sender_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
                             receiver_id BIGINT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
                             status friend_status DEFAULT 'PENDING' NOT NULL,
                             created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                             updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

                             CONSTRAINT check_not_self_friendship CHECK (sender_id <> receiver_id),
                             CONSTRAINT uq_friendship_sender_receiver UNIQUE (sender_id, receiver_id)
);

CREATE TABLE conversations (
                               id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                               type conv_type NOT NULL,
                               name VARCHAR(100), -- Chỉ dùng cho Group Chat
                               private_key VARCHAR(100) UNIQUE, -- Khóa tạo từ cặp ID (UserA_UserB) dùng định danh Chat 1-1 nhanh
                               created_by BIGINT REFERENCES users(id) ON DELETE SET NULL,
                               last_message_id BIGINT,
                               last_message_at TIMESTAMP WITH TIME ZONE,
                               avatar_url TEXT,
                               created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                               updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

                               CONSTRAINT check_private_conversation CHECK (
                                   (type = 'PRIVATE' AND name IS NULL AND avatar_url IS NULL AND private_key IS NOT NULL) OR
                                   (type = 'GROUP')
                                   )
);

CREATE TABLE messages (
                          id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                          conversation_id BIGINT NOT NULL REFERENCES conversations(id) ON DELETE CASCADE,
                          sender_id BIGINT NOT NULL REFERENCES users(id),
                          content TEXT,
                          type msg_type DEFAULT 'TEXT' NOT NULL,
                          reply_to_message_id BIGINT REFERENCES messages(id) ON DELETE SET NULL,
                          file_url TEXT,
                          file_name VARCHAR(255),
                          file_size BIGINT,
                          created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                          updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
                          is_deleted BOOLEAN DEFAULT FALSE NOT NULL,
                          is_edited BOOLEAN DEFAULT FALSE NOT NULL,
                          deleted_at TIMESTAMP WITH TIME ZONE,
                          deleted_by BIGINT REFERENCES users(id),

                          CONSTRAINT check_message_content CHECK (
                              is_deleted = TRUE OR content IS NOT NULL OR file_url IS NOT NULL
                              )
);

